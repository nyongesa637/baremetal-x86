builtin_program_t builtin_programs[MAX_PROGRAMS];
int builtin_program_count = 0;

static void register_program(const char *name, const uint8_t *data, uint32_t size, const char *desc) {
    if (builtin_program_count >= MAX_PROGRAMS) return;
    builtin_programs[builtin_program_count].name = name;
    builtin_programs[builtin_program_count].data = data;
    builtin_programs[builtin_program_count].size = size;
    builtin_programs[builtin_program_count].description = desc;
    builtin_program_count++;
}

void programs_install(void) {
    register_program("hello.elf", prog_hello, sizeof(prog_hello), "Print hello from ELF");
    register_program("count.elf", prog_count, sizeof(prog_count), "Count 1-5 on serial");

    // Write all programs to ramfs
    for (int i = 0; i < builtin_program_count; i++) {
        ramfs_write(builtin_programs[i].name,
                    (const char *)builtin_programs[i].data,
                    builtin_programs[i].size);
    }
}
